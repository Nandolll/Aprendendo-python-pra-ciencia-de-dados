#Fatiamento de strings é uma técnica utilizada para retornar substrings( partes da string original), informando inicio (start), fim (stop) e passo (step):[start: stop[,step]].

nome='Fernando lucas de azevedo lima'
print(nome[:])
print(nome[0])
print(nome[:8])
print(nome[9:])
print(nome[9:13])
print(nome[::-1])
